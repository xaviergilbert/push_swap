#include "libft.h"

char	*ft_strnew(size_t size)
{
	char				*str;
	unsigned int		i;

	if (!(str = (char*)malloc(sizeof(*str) * (size + 1))))
		return (0);
	i = 0;
	while (i < size)
	{
		str[i] = '\0';
		i++;
	}
	str[i] = '\0';
	return (str);
}
