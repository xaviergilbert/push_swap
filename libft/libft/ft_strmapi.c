#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*str;

	i = 0;
	if (s != NULL && f != NULL)
	{
		if (!(str = (char*)malloc(sizeof(*str) * (ft_strlen((char*)s) + 1))))
			return (0);
		while (s[i])
		{
			str[i] = f(i, s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	else
		return (0);
}
